			<p>
	Even during times where the economy is growing, the construction industry can be volatile. Unlike many other industries, construction company are often required to use the bidding process as well as rely on subcontractors that they only have limited control over. These factors can lead to surprising cost overruns that can sink established, profitable businesses. Even major privately held construction firms end up filing for Chapter 11 protection when overwhelmed by debt.

I talk with construction company owners all the time. They face many of the same issues that every business owner does. The cost of rent in New York is outrageous, and disagreements with suppliers happen. But most of the questions I get from construction company owners relate to unexpected cost overruns from projects that have gone awry. They include:

What will happen to equipment I am renting?

Can I get rid of or reduce credit card debt or revolving lines of credit?

Is it possible to restructure the business without filing bankruptcy?

These are important questions, especially because of the consequences of a bankruptcy for construction firms. A company that files for bankruptcy is typically barred from seeking state or federal contracts for a period of time after their discharge. If you work in the construction industry and have similar questions give me a call, because I would love to hear from you. 		
				</p>
<h3 class="text-blue-header">Restructuring a Construction Company through Chapter 11 Bankruptcy</h3>
				<p>	
If you are in a similar position to the construction companies I mentioned above, you likely have had some over budget projects send you spiraling into debt. That’s frustrating, as it can undo all of the good things going for your company.
				</p>
				<p>
For starters, filing for bankruptcy under chapter 11 of the bankruptcy code will buy you some time to get your affairs in order. That’s thanks to something called the automatic bankruptcy stay. At the moment you file for bankruptcy, the automatic stay halts all attempts by your creditors from attempting to collect from you. This can halt eviction proceedings and keep supplies from suing you over missed invoices.
				</p>
				<p>
The stay even applies to any equipment you may be leasing. While the rules leased business equipment are different than other debts, you will still have time to work out a plan in your bankruptcy proceeding before the company you lease from shows up to take the equipment back. For some construction companies, that can be the difference between staying in business and shutting the doors forever. 				
				</p>
<h3 class="text-blue-header">Will Chapter 11 give your construction business a solid foundation to build on?</h3>
				<p>				
The needs of every business are different. That’s why the decision to declare chapter 11 bankruptcy takes a data-driven review of your company’s financial position. Sometimes, an out-of-court workout can be in the best interest of a company. But in many cases a chapter 11 bankruptcy and the automatic stay that comes with it is the only chance a business has to reorganize and continue operating. If you think bankruptcy might be right option for your business, contact <a class="plain-text-link" title="About Bradley Bailyn" href="https://bailynlaw.com/">Bailyn Law</a> today.
					
